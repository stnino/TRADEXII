    private void getRideInformation() {
        historyRideInfoDb.addListenerForSingleValueEvent(new ValueEventListener() {

            //RIDE_ID IS THE SAME AS THE HISTORY.GETKEY() ID IN THE PREVIOUS ONES LIKE DRIVERMAP ACTIVITY

            @SuppressLint("SetTextI18n")
            @Override
            public void onDataChange(DataSnapshot dataSnapshot) {
                if(dataSnapshot.exists()){
                    for (DataSnapshot child:dataSnapshot.getChildren()){

                        if (child.getKey().equals("customer")){

                            //HERE WE ARE JUST IDENTIFYING WHETHER IT IS THE CUSTOMER OR THE DRIVER MAKING OR CHECKING HIS HISTORY
                            // WE CHECK TO SEE IF THE CURRENT USER ID IS THE SAME AS THAT OF THE CUSTOMER
                            customerId = child.getValue().toString();
                            if(!customerId.equals(currentUserId)){
                                userDriverOrCustomer = "Drivers";
                                getUserInformation("Customers", customerId);
                            }
                        }

                        //WE CHECK IF THE ID IS REALLY THE DRIVER ID AND WE COMPARE WITH THE CURRENT USER TO SEE IF HE IS A USERE OR NOT
                        // THIS IS THE WAY WE KNOW THAT DRIVER
                        if (child.getKey().equals("driver")){
                            driverId = child.getValue().toString();
                            if(!driverId.equals(currentUserId)){
                                userDriverOrCustomer = "Customers";

                                // WE HAVE TO REVIEW THE GET USER INFORMATION
                                getUserInformation("Drivers", driverId);
                                // HE HAS TO DISPLAY THE CUSTOMER RELATED OBJECTS SUCH AS THE PRODUCT HE OR SHE WILL WANT TO BUY
                                displayCustomerRelatedObjects();
                            }
                        }
                        if (child.getKey().equals("timestamp")){
                            rideDate.setText(getDate(Long.valueOf(child.getValue().toString())));
                        }
                        if (child.getKey().equals("rating")){
                            mRatingBar.setRating(Integer.valueOf(child.getValue().toString()));

                        }
                        if(child.getKey().equals("triptime")){

                            triptime = (int) child.getValue();
                            triptimecalc = Double.valueOf(triptime);
                            triptimes = String.valueOf(triptimecalc);
                        }
                        if (child.getKey().equals("customerPaid")){
                            customerPaid =true;
                        }
                        if (child.getKey().equals("distance")){
                            distance = child.getValue().toString();




                        }
                        if (child.getKey().equals("baseprice")){
                             baseprice = (Double) child.getValue();

                            rideDistance.setText(distance.substring(0, Math.min(distance.length(), 5)) + " km");

                            //THIS IS THE PRICE CUSTOMER HAS TO  PAY , HE OR SHE HAS TO PAY
                            // DRIVER OR SELLER IS GIVEN A DISCOUNT SO 0.4 AND HE OR SHE PAYS
                            //WITH PAYPAL
                            ridePrice = Double.valueOf(distance) * 0.5 + baseprice;

                        }



                        if (child.getKey().equals("destination")){
                            rideLocation.setText(child.getValue().toString());
                        }
                        if (child.getKey().equals("location")){
                            pickupLatLng = new LatLng(Double.valueOf(child.child("from").child("lat").getValue().toString()), Double.valueOf(child.child("from").child("lng").getValue().toString()));
                            destinationLatLng = new LatLng(Double.valueOf(child.child("to").child("lat").getValue().toString()), Double.valueOf(child.child("to").child("lng").getValue().toString()));
                            if(destinationLatLng != new LatLng(0,0)){
                                getRouteToMarker();

                                // WE HAVE TO ALSO SPECIFY THE TOWN BUT THAT IS FOR A LATER WORK
                            }
                        }
                    }
                }
            }
            @Override
            public void onCancelled(DatabaseError databaseError) {
            }
        });
    }

